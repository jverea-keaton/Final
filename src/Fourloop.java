public class Fourloop {

    public static void displayFourloop() {
        int user= 0;
        String result;
        int random = (int) (Math.random() * 3 + 1);

        while (user != 999) {
            if (user == 1) {
                if (random == 1)
                    result = "";
                else if (random == 2)
                    result = "";
                else
                    result = "";
            } else if (user == 2) {
                if (random == 1) {
                    result = "";
                }
            }
        }
    }
}